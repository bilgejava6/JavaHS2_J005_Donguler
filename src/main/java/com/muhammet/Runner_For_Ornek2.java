package com.muhammet;

public class Runner_For_Ornek2 {
    public static void main(String[] args) {
        /**
         * 1,6,11,16,21......391 şeklinde giden bir listeyi
         * for döngüsü kullanarak ekrana yazan kodu yazınız.
         */
        for (int i=1;i<=391;i+=5){
            System.out.println(i);
        }
    }
}
