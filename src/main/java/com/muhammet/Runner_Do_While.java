package com.muhammet;

public class Runner_Do_While {
    public static void main(String[] args) {
        /**
         * do...while(condition);
         * while ile birebir aynıdır sadece kod bloğu koşuldan önce olduğu için
         * koşul sağlamasa bile kod bloğu en az birkere çalışır.r
         */
        int secim=0;
        do{
            //body
            System.out.println("Deneme");
        }while (secim!=0);
    }
}
