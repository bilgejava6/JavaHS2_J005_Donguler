package com.muhammet;

public class Runner_For_Ornek1 {
    public static void main(String[] args) {
        /**
         * 0....40 a kadar olan sayıları ekrana yazdırınız.
          */
        for(int i=0;i<=40;i++){ // i=0, i=1, i=2 .....i=38, i=39, i=40
            System.out.println(i);
        }

        /**
         * 20....112- (20,112) sayıları arasındaki değerleri ekrana yazdıran for
         * döngüsünü yazınız.
         */
        for(int i=21;i<112;i++){
            System.out.println(i);
        }

    }
}
